async function main() {
  console.log("=== Seed: создание категорий и тегов ===\n");

  const categoryMap: Record<string, string> = {};

  // Категории по авторам/происхождению
  for (const s of stories) {
    const catName = s.category;
    if (categoryMap[catName]) continue;

    const slug = catName
      .toLowerCase()
      .replace(/\s+/g, "-")
      .replace(/[^a-z0-9а-яё-]/gi, "");

    const cat = await prisma.category.upsert({
      where: { slug },
      update: { description: categoryDescriptions[catName] || "" },
      create: {
        name: catName,
        slug,
        description: categoryDescriptions[catName] || "",
      },
    });
    categoryMap[catName] = cat.id;
    console.log(`  [+] Категория: ${catName}`);
  }

  // Возрастные категории
  for (const ac of ageCategories) {
    const cat = await prisma.category.upsert({
      where: { slug: ac.slug },
      update: {},
      create: { name: ac.name, slug: ac.slug, description: ac.description },
    });
    categoryMap[ac.name] = cat.id;
    console.log(`  [+] Категория: ${ac.name}`);
  }

  // Тематические категории
  for (const tc of themeCategories) {
    const cat = await prisma.category.upsert({
      where: { slug: tc.slug },
      update: {},
      create: { name: tc.name, slug: tc.slug, description: tc.description },
    });
    categoryMap[tc.name] = cat.id;
    console.log(`  [+] Категория: ${tc.name}`);
  }

  console.log("\n=== Seed: создание сказок ===\n");

  let created = 0;
  let skipped = 0;

  for (const s of stories) {
    const existing = await prisma.story.findUnique({ where: { slug: s.slug } });
    if (existing) {
      console.log(`  [skip] "${s.title}" уже существует`);
      skipped++;
      continue;
    }

    const paragraphs = s.storyText
      .split(/\n\n+/)
      .filter((p) => p.trim())
      .map((p) => p.trim());

    const wordsCount = s.storyText.split(/\s+/).length;
    const readingTime = Math.max(1, Math.ceil(wordsCount / 150));

    // Определяем возрастные категории
    const ageCategoryNames: string[] = [];
    if (s.ageMin <= 3) ageCategoryNames.push("Для детей 0-3 лет");
    if (s.ageMin <= 5 && s.ageMax >= 3) ageCategoryNames.push("Для детей 3-5 лет");
    if (s.ageMin <= 8 && s.ageMax >= 5) ageCategoryNames.push("Для детей 5-8 лет");
    if (s.ageMax >= 8) ageCategoryNames.push("Для детей 8-12 лет");

    // Определяем тематические категории
    const themeCategoryNames: string[] = [];
    if (readingTime <= 5) themeCategoryNames.push("Короткие");
    if (readingTime > 15) themeCategoryNames.push("Длинные");
    if (s.tags.includes("дружба") || s.tags.includes("доброта")) themeCategoryNames.push("Поучительные");
    if (s.tags.includes("животные")) themeCategoryNames.push("Про животных");
    if (s.tags.includes("приключения") || s.tags.includes("лес")) themeCategoryNames.push("Приключения");
    if (s.tags.includes("волшебство") || s.tags.includes("волшебный")) themeCategoryNames.push("Волшебные");
    if (s.tags.includes("юмор") || s.tags.includes("забавная") || s.tags.includes("весёлая")) themeCategoryNames.push("Юмор");
    if (s.ageMax <= 5) themeCategoryNames.push("Сказки на ночь");

    // Собираем все категории
    const allCategoryNames = [s.category, ...ageCategoryNames, ...themeCategoryNames];
    const categoryIds = allCategoryNames
      .map((name) => categoryMap[name])
      .filter(Boolean);

    // Создаём сказку с первой категорией (основной)
    const story = await prisma.story.create({
      data: {
        title: s.title,
        slug: s.slug,
        storyText: s.storyText,
        readingTimeMinutes: readingTime,
        status: "published",
        metadata: JSON.stringify({
          age_min: s.ageMin,
          age_max: s.ageMax,
          difficulty: s.difficulty,
          seo_title: s.seoTitle,
          seo_description: s.seoDescription,
          seo_keywords: s.seoKeywords,
        }),
        categoryId: categoryIds[0] || null,
        paragraphs: {
          create: paragraphs.map((content, i) => ({
            paragraphIndex: i,
            content,
            hasImage: false,
          })),
        },
        seoMetadata: {
          create: {
            seoTitle: s.seoTitle,
            seoDescription: s.seoDescription,
            keywords: JSON.stringify(s.seoKeywords.split(",").map((k) => k.trim())),
          },
        },
      },
    });

    // Связываем сказку со всеми подходящими категориями
    for (const catId of categoryIds) {
      await prisma.storyCategory.create({
        data: { storyId: story.id, categoryId: catId },
      });
    }

    // Создаём теги
    const tagNames = s.tags.split(",").map((t) => t.trim());
    for (const tagName of tagNames) {
      const tagSlug = tagName
        .toLowerCase()
        .replace(/\s+/g, "-")
        .replace(/[^a-z0-9а-яё-]/gi, "");

      const tag = await prisma.tag.upsert({
        where: { slug: tagSlug },
        update: { usageCount: { increment: 1 } },
        create: { name: tagName, slug: tagSlug, usageCount: 1 },
      });

      await prisma.storyTag.create({
        data: { storyId: story.id, tagId: tag.id },
      });
    }

    created++;
    console.log(`  [+] "${s.title}" (${readingTime} мин, ${paragraphs.length} абзацев, ${tagNames.length} тегов)`);
  }

  console.log(`\n=== Готово! ===`);
  console.log(`  Создано: ${created} сказок`);
  console.log(`  Пропущено: ${skipped} (уже существуют)`);
  console.log(`  Всего: ${created + skipped}`);
}

main()
  .catch(console.error)
  .finally(() => prisma.$disconnect());
